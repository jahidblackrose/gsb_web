                          <p> <b>ABOUT PRME </b> </p>
                          <p align="justify">Launched at the 2007 UN Global Compact Leaders Summit in Geneva, the Principles for Responsible Management Education (PRME) initiative is the first organised relationship between the United Nations and business schools, with the PRME Secretariat housed in the UN Global Compact Office. </p>

                          <p>The mission of PRME is to inspire and champion responsible management education, research and thought leadership globally. </p>

                          <p align="justify">The Six Principles of PRME are inspired by internationally accepted values, such as the United Nations Global Compact's Ten Principles, and provide an engagement structure for academic institutions to advance social responsibility through incorporating universal values into curricula and research. They seek to establish a process of continuous improvement among institutions of management education in order to develop a new generation of business leaders capable of managing the complex challenges faced by business and society in the 21st century. </p>

                          <p align="justify">The PRME initiative serves as a framework for gradual, systemic change in business schools and management-related institutions, based on three distinctive characteristics of the initiative: continuing improvement, a learning network, and reporting progress to stakeholders. Reporting on progress by participant institutions to stakeholders through Sharing Information on Progress (SIP) reports is an essential part of the active commitment to PRME. SIP reporting provides information on progress achieved in the implementation of PRME, an opportunity for exchanging effective practices, and creates a learning community with other participating institutions. </p>

