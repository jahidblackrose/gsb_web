                  <p> INTRODUCTION</p>
            
            
            <p align="justify">
            Universiti Sains Malaysia (USM) offers a Doctor of Philosophy (PhD) programme in Hong Kong through the cooperation and collaboration between USM’s Graduate School of Business (GSB) and the Hong Kong Management Association (HKMA). The purpose of the programme is to provide education and research training in business, leading leading to the award of the Doctor of Philosophy (PhD) degree by USM.</p>

<p align="justify">Candidates are expected to pursue a structured and supervised research agenda in one of the many business-related fields in which GSB has expertise. Candidates will undergo training and workshops  on research methodology and academic writing. Prior to the submission of their final theses, candidates need to present and successfully defend their conceptual papers and research proposals within the duration stipulated. </p>

<p align="justify">At the end of their candidature, candidates are expected to write a full thesis and demonstrate a convincing and notable contribution to knowledge and to illustrate a strong capacity to pursue scholarly research. The research should be of a standard appropriate for publication in a reputable scholarly journal. </p> 
</div>
                  <div class="tab-pane fade" id="tab2success">
                  
                 <p>  The PhD@GSB programme aims to achieve the following goals: </p>

<p>i.	Produce business scholars who possess advanced knowledge in their business disciplines and who can integrate critical business issues with scientific research.</p>
<p>ii.	Produce business scholars with the ability to develop critical and innovative thinking and problem-solving.</p>
<p>iii.	Develop business scholars with effective and persuasive communication.</p>
<p>iv.	Develop business scholars with ethical and responsible research practices.</p>
<p>v.	Produce business scholars with a high awareness of business sustainability.</p>
<p>vi.	Produce business scholars with the relevant skills to conduct solid scientific research.</p>
<p align="justify">The ultimate learning outcome for PhD@GSB graduates is the ability to synthesise multi-disciplinary knowledge through an in-depth understanding of each discipline and mastery of research skills in the related field of study and to solve real business problems. Hence, upon completion of their PhD, graduates should be able:</p>
<p>i.	To apply advanced knowledge of the relevant business disciplines and to integrate the issues with relevant business research methods.</p>
<p>ii.	To demonstrate creativity, critical and ethical thinking in analysing, synthesising and solving complex business problems.</p>
<p>iii.	To display high proficiency skills in written and spoken communication.</p>
<p>iv.	To demonstrate ethical and responsible research practices.</p>
<p>v.	To comprehend business issues through the lens of business sustainability.</p>
<p>vi.	To provide knowledge of research techniques and tools that can be utilizsd to conduct scientific research.</p>
                  
