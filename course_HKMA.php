     <p align="justify">   Candidates registered on the PhD programme in  Hong Kong are deemed full-time USM students. All candidates must attend the research workshops and video conferencing as scheduled. The complete list of meetings and activities is available in Appendix 1. The workshops and video conferences serve as a knowledge-sharing platform for a variety of activities for GSB PhD candidates and academic staff. The activities involve communication, discussions and presentations, progress reviews, proposal and findings defence, and a general research training programme. </p>

<p> <b>4.1.	Research Training and Writing Workshops </b> </p>

<p align="justify">All candidates are required to participate in all the workshops that are specially organised to develop students’ research and writing skills. These workshops are designed to enhance candidates’ knowledge in conducting research.</p>

<p align="justify">The first workshop will equip candidate with a background in research methodology, while the second workshop emphasises the skills needed in preparing their thesis write-up and reporting.</p>

